### Compound Assignment
# Compound assignment คือตัวดำเนินการที่ใช้เพื่ออัพเดทหรือแก้ไขค่าปัจจุบันของตัวแปร
# โดยการกระทำทางคณิตศาสตร์และใช้ตัวดำเนินการกำหนดค่าร่วมด้วย 
# ซึ่งตัวดำเนินการแบบ Compound assignment มักจะใช้เป็นรูปแบบสั้นของตัวดำเนินการ
# ทางคณิตศาสตร์และตัวดำเนินการระดับบิตเพื่อให้การเขียนสั้นลง

x=10
print("Before Compound Assignment: ",x)
x+=5 # x=x+5
# x-=15 # x = x-15
# x*=5 # x=x*5
# x/=5 # x=x/5
# x//=5
# x**=2 # x= x**2
# x%=3
print("After Compound Assignment: ",x)