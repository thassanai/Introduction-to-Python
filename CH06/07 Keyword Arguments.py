### Keyword Arguments 
### เพื่อ mapping variable ให้ตรงกับตัวแปร ไม่ว่าจะเรียงลำดับอย่างไร
def displayData(fname,lname,city):
    print("ชื่อ = ",fname)
    print("นามสกุล = ",lname)
    print("จังหวัด = ",city)

# displayData("ระยอง","ใจดี","นัท")
displayData(city="เพชรบูรณ์",lname="bmw",fname="z4")
displayData(lname="หล่อมาก",city="พะเยา",fname="สมชาย")
