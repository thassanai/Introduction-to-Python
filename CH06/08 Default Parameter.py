### Default Parameter กำหนดค่าเริ่มต้นของ parameter
def displayData(fname,lname,city="กรุงเทพ"):
    print("ชื่อ = ",fname)
    print("นามสกุล = ",lname)
    print("จังหวัด = ",city)

# displayData("ระยอง","ใจดี","นัท")
displayData(lname="bmw",fname="z4")
displayData(lname="หล่อมาก",city="พะเยา",fname="สมชาย")
